USER TABLE:
CREATE TABLE DTS_USER (
   FIRST_NAME VARCHAR(100) DEFAULT NULL,
   MIDDLE_NAME VARCHAR(100) DEFAULT NULL,
   LAST_NAME VARCHAR(100) DEFAULT NULL,
   MOBILE_NO VARCHAR(50) NOT NULL,
   EMAIL VARCHAR(100) DEFAULT NULL,
   USER_NAME VARCHAR(100) NOT NULL,
   PASSWORD VARCHAR(100) NOT NULL,
   ALTERNATE_MOBILE_NO VARCHAR(50) DEFAULT NULL,
   CREATED_AT DATETIME DEFAULT NULL,
   UPDATED_AT DATETIME DEFAULT NULL,
   USER_ID VARCHAR(100) NOT NULL,
   ADDRESS_ID VARCHAR(100) DEFAULT NULL,
   PRIMARY KEY (USER_ID),
   KEY FK_DTS_USER_ADDRESS (ADDRESS_ID),
   CONSTRAINT FK_DTS_USER_ADDRESS FOREIGN KEY (ADDRESS_ID) REFERENCES DTS_ADDRESS (ADDRESS_ID)
 );

