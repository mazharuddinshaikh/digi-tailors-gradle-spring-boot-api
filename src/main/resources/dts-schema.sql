   CONSTRAINT FK_USER_CUSTOMER FOREIGN KEY (USER_ID) REFERENCES DTS_USER (USER_ID)
 );

DRESS TYPE:
CREATE TABLE DTS_DRESS_TYPE (
  TYPE_ID VARCHAR(50) NOT NULL,
  TYPE_NAME VARCHAR(100) NOT NULL,
  TYPE_DESCRIPTION VARCHAR(500) DEFAULT NULL,
  COMMENT VARCHAR(500) DEFAULT NULL,
  CREATED_AT DATETIME DEFAULT NULL,
  UPDATED_AT DATETIME DEFAULT NULL,
  CREATED_BY VARCHAR(50) DEFAULT NULL,
  PRIMARY KEY (TYPE_ID),
  KEY CREATED_BY (CREATED_BY),
  CONSTRAINT DTS_DRESS_TYPE_IBFK_1 FOREIGN KEY (CREATED_BY) REFERENCES DTS_USER (USER_ID)
);

USER DRESS TYPE:
CREATE TABLE DTS_USER_DRESS_TYPE (
  PRICE DOUBLE DEFAULT 0,
  CREATED_AT DATETIME DEFAULT NULL,
  UPDATED_AT DATETIME DEFAULT NULL,
  UDT_ID VARCHAR(50) NOT NULL,
  DRESS_TYPE_ID VARCHAR(50) DEFAULT NULL,
  PRIMARY KEY (UDT_ID),
  KEY DRESS_TYPE_ID (DRESS_TYPE_ID),
  CONSTRAINT DTS_USER_DRESS_TYPE_IBFK_1 FOREIGN KEY (DRESS_TYPE_ID) REFERENCES DTS_DRESS_TYPE (TYPE_ID)
);

DRESS TABEL:
CREATE TABLE DTS_DRESS (
  DRESS_ID VARCHAR(50) NOT NULL,
  CREATED_AT DATETIME DEFAULT NULL,
  UPDATED_AT DATETIME DEFAULT NULL,
  COMMENT VARCHAR(500) DEFAULT NULL,
  USER_ID VARCHAR(50) DEFAULT NULL,
  SHOP_ID VARCHAR(50) DEFAULT NULL,
  CUSTOMER_ID VARCHAR(50) DEFAULT NULL,
  PRIMARY KEY (DRESS_ID),
  KEY USER_ID (USER_ID),
  KEY SHOP_ID (SHOP_ID),
  KEY CUSTOMER_ID (CUSTOMER_ID),
  CONSTRAINT DTS_DRESS_IBFK_1 FOREIGN KEY (USER_ID) REFERENCES DTS_USER (USER_ID),
  CONSTRAINT DTS_DRESS_IBFK_2 FOREIGN KEY (SHOP_ID) REFERENCES DTS_SHOP (SHOP_ID),
  CONSTRAINT DTS_DRESS_IBFK_3 FOREIGN KEY (CUSTOMER_ID) REFERENCES DTS_CUSTOMER (CUSTOMER_ID)
);


